//Part 1: Variables and Naming

var foo = 100;
console.log('foo=' + foo);

var bar = "200"; 
console.log('bar=' + bar);

var fooBar = foo + bar; 
console.log('foo+bar=' + fooBar);

var barFoo = bar + foo;
console.log('bar+foo=' + barFoo);

var fooHundo = foo * 2;
console.log('foo * 2=' + fooHundo);

var barHundo = bar * 2;
console.log('bar * 2=' + barHundo);
